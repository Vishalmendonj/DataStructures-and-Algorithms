package PatternMatching;

public class SequenceHalfPyramid {
    public static void main(String[] args) {
        int i,j;
        int c=4;
        int n=1;
        int h=c-3;
        int num=1;
    for(i=1;i<=5;i++){
        for(j=n; j<=h; j++){
            System.out.print(num);
            num++;
            
        }
        h++;

        System.out.println();
        

    }
    }
}
